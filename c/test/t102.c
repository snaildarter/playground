#include <stdio.h>

int main()
{
    double a, b, c, t;
    scanf("%lf%lf%lf%lf", &a, &b, &c, &t);
    printf("%.2lf\n", (1 - t / a - t / b) / (1 / a + 1 / b - 1 / c) + t);
    return 0;
}
